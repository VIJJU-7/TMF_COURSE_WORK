

public class Wallet {
    private int defaultBal;
	private int userId;
	private int limit;
	public int getLimit() {
		return limit;
	}
	public void setLimit(int limit) {
		this.limit = limit;
	}
	public int getDefaultBal() {
		return defaultBal;
	}
	public void setDefaultBal(int defaultBal) {
		this.defaultBal = defaultBal;
	}
	public int getUserId() {
		return userId;
	}
	public void setUserId(int userId) {
		this.userId = userId;
	}

	
}